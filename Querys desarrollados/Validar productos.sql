------------------------ EXTRACCION PRODUCTOS ----------------------------------
SELECT 
B.PART_NUM AS CODIGO,
B.NAME AS NOMBRE,
A.ASSET_NUM AS NUMERO_PRODUCTO,
A.STATUS_CD AS ESTADO
FROM SIEBEL.S_ASSET A
LEFT JOIN SIEBEL.S_PROD_INT B ON A.PROD_ID = B.ROW_ID
WHERE 1 = 1
AND B.PART_NUM IN ('')
;

